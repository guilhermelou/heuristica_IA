function Region()
{
	var x, y, width, height;

	this.setX = function(x)
	{
		this.x = x;
	};

	this.setY = function(y)
	{
		this.y = y;
	};

	this.setWidth = function(width)
	{
		this.width = width;
	};

	this.setHeight = function(height)
	{
		this.height = height;
	};

	this.getX = function()
	{
		return this.x;
	};

	this.getX = function()
	{
		return this.x;
	};

	this.getY = function()
	{
		return this.y;
	};

	this.getWidth = function()
	{
		return this.width;
	};

	this.getHeight = function()
	{
		return this.height;
	};
}